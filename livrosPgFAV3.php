        <p>“O Guia do Mochileiro das Galáxias” foi uma leitura gostosa e simplesmente maravilhosa, que me fez rir sozinha em diversas situações, mas também refletir sobre muita coisa. Isso sem falar que o livro me fez viajar pelas estrelas junto com os personagens, fiquei me imaginando no espaço, viajando junto com eles. Estou super ansiosa pelos outros livros e gostei tanto desse que estou tentada a começar relê-lo agora mesmo. O livro realmente me conquistou e garantiu seu lugar entre os meus favoritos, assim como o próprio Adams. É uma pena que o autor já tenha falecido e não possa mais escrever histórias incríveis e tão gostosas de se ler.</p>
